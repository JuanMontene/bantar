function myFunction() {
  document.getElementById('myDropdown').classList.toggle('show');

  var plusIcon = document.querySelector('.plus-icon');
  var minusIcon = document.querySelector('.minus-icon');
  plusIcon.classList.toggle('hidden');
  minusIcon.classList.toggle('show');
}

window.onclick = function (event) {
  if (!event.target.matches('.drop-info')) {
    var dropdowns = document.getElementsByClassName('button-content');
    var i;
    for (i = 0; i < dropdowns.length; i++) {
      var openDropdown = dropdowns[i];
      if (openDropdown.classList.contains('show')) {
        openDropdown.classList.remove('show');

        var plusIcon = document.querySelector('.plus-icon');
        var minusIcon = document.querySelector('.minus-icon');
        plusIcon.classList.toggle('hidden');
        minusIcon.classList.toggle('show');
      }
    }
  }
};

